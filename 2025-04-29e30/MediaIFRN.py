# Calcular média da disciplina do IFRN

# 1. Solicitar ao usuário que informe duas notas
# 2. Calcular a média do IFRN
# 3. Exibir a média

Etapa1 = float(input('Digite a nota da Etapa1: '))

Etapa2 = float(input('Digite a nota da Etapa2: '))

Média = (Etapa1 * 2 + Etapa2 * 3) / 5

print(f'A Média é: {Média:.0f}')
